public class casting {
    public static void main(String[] args) {
        double a = 10;
        int b = (int) (a + 8.5);
        int age = 15;
        age = 17;
        age = 20;
        final float pi = 1.27F;
        System.out.println(b);
        System.out.println(age);
        System.out.println(pi);
    }
}
